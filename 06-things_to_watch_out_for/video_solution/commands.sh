# The commands that are used in the `Things To Watch Out For` video

# Create two folders - `hello` and `world`
mkdir hello world

# Delete each directory
rm -rf hello
rm -rf world

# Or, delete both at once
rm -rf hello world

# Create two files - `hello` and `world.txt`
nano hello world.txt

# Delete each file
rm hello
rm world.txt

# Or, delete both at once
rm hello world.txt