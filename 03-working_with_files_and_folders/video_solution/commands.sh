# The commands that are used in the `Working With Files and Folders` video

# Create a file called `hello.txt`
touch hello.txt

# Delete a file called `hello.txt`
rm hello.txt

# Make a directory called `hello`
mkdir hello

# Copy `hello.txt` into `hello`
cp hello.txt hello

# Move `hello.txt` into `hello`
mv hello.txt hello

# Delete a directory called `hello`
rm -rf hello